#define EMOJI_TABLE_COUNT ((int)(sizeof(emoji_table) / sizeof(emoji_table[0])))

/*
 * Look up a Unicode codepoint in the emoji table.
 * Returns a pointer to the 8×16 bitmap on match, NULL otherwise.
 */
static inline const uint8_t* emoji_lookup(uint32_t cp) {
    int lo = 0, hi = EMOJI_TABLE_COUNT - 1, mid;
    while (lo <= hi) {
        mid = (lo + hi) / 2;
        if (emoji_table[mid].codepoint == cp)
            return emoji_table[mid].bitmap;
        if (cp < emoji_table[mid].codepoint)
            hi = mid - 1;
        else
            lo = mid + 1;
    }
    return (const uint8_t*)0;
}

